// Helper function to get team logo URL
export const getTeamLogo = (teamCode) => {
  const team = NFL_TEAM_LOGOS[teamCode?.toUpperCase()];
  return team ? team.logo : null;
};

// Helper function to get team logo URL (small/dark version)
export const getTeamLogoSmall = (teamCode) => {
  const team = NFL_TEAM_LOGOS[teamCode?.toUpperCase()];
  return team ? team.logoSmall : null;
};

// Helper function to get team name
export const getTeamName = (teamCode) => {
  const team = NFL_TEAM_LOGOS[teamCode?.toUpperCase()];
  return team ? team.name : teamCode;
};

// Helper function to get team primary color
export const getTeamPrimaryColor = (teamCode) => {
  const team = NFL_TEAM_LOGOS[teamCode?.toUpperCase()];
  return team ? team.primaryColor : '#666666';
};

// Helper function to get team secondary color
export const getTeamSecondaryColor = (teamCode) => {
  const team = NFL_TEAM_LOGOS[teamCode?.toUpperCase()];
  return team ? team.secondaryColor : '#999999';
};

// TeamLogo component for easy use in React
export const TeamLogo = ({ teamCode, size = 24, className = '' }) => {
  const logoUrl = getTeamLogo(teamCode);
  const teamName = getTeamName(teamCode);
  
  if (!logoUrl) {
    return <span className={className}>{teamCode}</span>;
  }
  
  return (
    <img 
      src={logoUrl} 
      alt={teamName}
      width={size}
      height={size}
      className={`object-contain ${className}`}
      onError={(e) => {
        e.target.style.display = 'none';
      }}
    />
  );
};

export default NFL_TEAM_LOGOS;
